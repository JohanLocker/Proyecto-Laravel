    {
       $Valid = DB::table('Usuarios')->where('usuario','=', $request->input('usuario'))->where('contrasena','=', $request->input('contrasena'))->get()->first();
       if(count($Valid) >= 1){
        return view('Inicio.Inicio', ['Mensajes'=>'Iniciado sesion con exito']);
       }
       return back()->with('Mensajes',"Usuario o contrasena incorrectos");
